public class DataT {
    public static void main(String args[])
    {
        //1)PRIMITIVE DATA TYPE
        byte a = 127;
        short b = 32767;
        int c = 34567; //stores 4bytes
        long d = 7398; //stores 8bytes
        /* The above data types can contain -ve values */
        float e = 5.6789f; //upto 7 deci
        double f = 3.89457329; // upto 15 deci
        char g = 'z';
        boolean i = true ;//false 
         //2)REFERENCE DATA TYPE (String, Array, Class Objects)
        String h = "Rakshana";
        System.out.print(h.charAt(5));
    }
}
